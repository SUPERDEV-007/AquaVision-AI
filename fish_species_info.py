def get_species_info(species_name):
    """
    Get detailed information about a fish species
    
    Args:
        species_name: Name of the fish species
    
    Returns:
        Dictionary with species information or None
    """
    # Try exact match first
    if species_name in FISH_SPECIES_INFO:
        return FISH_SPECIES_INFO[species_name]
    
    # Try case-insensitive match
    species_lower = species_name.lower()
    for key in FISH_SPECIES_INFO.keys():
        if key.lower() == species_lower:
            return FISH_SPECIES_INFO[key]
    
    return None


def get_all_species_names():
    """Get list of all available species names"""
    return list(FISH_SPECIES_INFO.keys())
